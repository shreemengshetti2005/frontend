// Helper functions for consistent styling
export const getCardSizeClasses = (size: 'small' | 'medium' | 'large') => {
  const sizeMap = {
    small: 'col-span-1 row-span-1',
    medium: 'col-span-1 md:col-span-2 row-span-1',
    large: 'col-span-1 md:col-span-2 lg:col-span-3 row-span-2',
  };
  return sizeMap[size];
};

export const getScientificColor = (intensity: 'light' | 'medium' | 'dark' = 'medium') => {
  const colorMap = {
    light: visualTokens.colors.primary[300],
    medium: visualTokens.colors.primary[600],
    dark: visualTokens.colors.primary[800],
  };
  return colorMap[intensity];
};

